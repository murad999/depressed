<h2>Risk Factors</h2>
<p>Depression is one of the most common mental disorders in the U.S. Current research suggests that depression is caused by a combination of genetic, biological, environmental, and psychological factors.</p>
<p>Depression can happen at any age, but often begins in adulthood. Depression is now recognized as occurring in children and adolescents, although it sometimes presents with more prominent irritability than low mood. Many chronic mood and anxiety disorders in adults begin as high levels of anxiety in children.</p>
<p>Depression, especially in midlife or older adults, can co-occur with other serious medical illnesses, such as diabetes, cancer, heart disease, and Parkinsonâ€™s disease. These conditions are often worse when depression is present. Sometimes medications taken for these physical illnesses may cause side effects that contribute to depression. A doctor experienced in treating these complicated illnesses can help work out the best treatment strategy.</p>
<p>Risk factors include:</p>
<ul>
	<li>Personal or family history of depression</li>
	<li>Major life changes, trauma, or stress</li>
	<li>Certain physical illnesses and medications</li>
</ul>
